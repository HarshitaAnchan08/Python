#Program to Swap first and last elements in a list

#Create a list
list1=[23,65,67,87,90]

#Printing Result
print("The list before Swapping : ",list1)

#Interchanging first and last element
list1[0],list1[-1]=list1[-1],list1[0]

#Printing Result
print("After Swapping first and last elements in a list is : ",list1)
