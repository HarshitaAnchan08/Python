#Swapping of two numbers using third variable

#Input numbers
a=int(input("Enter first number : "))
b=int(input("Enter second number : "))
print("\nBefore Swapping : ")
print("\na = ",a)
print("\nb = ",b)

#logical section
temp=a
a=b
b=temp

#printing after swapping of numbers
print("\nAfter Swapping : ")
print("\na = ",a)
print("\nb = ",a)



